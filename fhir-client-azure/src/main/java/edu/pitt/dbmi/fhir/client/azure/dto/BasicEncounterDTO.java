package edu.pitt.dbmi.fhir.client.azure.dto;

import java.io.Serializable;

/**
 *
 * May 29, 2022 1:13:55 AM
 *
 * @author Kevin V. Bui (kvb2univpitt@gmail.com)
 */
public class BasicEncounterDTO implements Serializable {

    private static final long serialVersionUID = 6840069922049962090L;

    private String id;
    private String type;
    private String status;
    private String start;
    private String end;

    public BasicEncounterDTO() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getStart() {
        return start;
    }

    public void setStart(String start) {
        this.start = start;
    }

    public String getEnd() {
        return end;
    }

    public void setEnd(String end) {
        this.end = end;
    }

}
