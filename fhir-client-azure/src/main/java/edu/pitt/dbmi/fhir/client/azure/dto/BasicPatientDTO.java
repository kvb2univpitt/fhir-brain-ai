package edu.pitt.dbmi.fhir.client.azure.dto;

import java.io.Serializable;

/**
 * This class is used to hold basic patient information.
 *
 * May 11, 2022 1:50:12 PM
 *
 * @author Kevin V. Bui (kvb2univpitt@gmail.com)
 */
public class BasicPatientDTO implements Serializable {

    private static final long serialVersionUID = 1332352074156350382L;

    private String id;
    private String lastName;
    private String firstName;
    private String gender;

    public BasicPatientDTO() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

}
